"""End-to-End Triple-Provider Acceptance driver.

Runs the complete platform flow (idea -> FS -> analysis -> refine -> tasks ->
impact -> collab -> build -> exports) three times (once per provider), then
runs the reverse-FS pipeline three times against the first project's build
artifacts, then exercises every remaining MCP surface and writes a
report to ``reports/e2e-final.md``.

Each phase is idempotent and persisted to ``.e2e_runtime.json`` so reruns
pick up where they left off. Failures are captured into runtime.repairs
and retried via the repair loop.

Usage::

    python -m scripts.e2e_full              # run every enabled phase
    python -m scripts.e2e_full --phases=preflight,project_api
    python -m scripts.e2e_full --reset       # wipe runtime first
"""

