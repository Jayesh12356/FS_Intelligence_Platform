# ── Phase registry ────────────────────────────────────────────────────


async def main(phases: list[str] | None, reset_runtime: bool) -> int:
    setup_logging()
    if reset_runtime:
        from scripts._e2e_utils import RUNTIME_PATH

        RUNTIME_PATH.unlink(missing_ok=True)
    rt = Runtime.load()
    driver = Driver(rt)

    async with BackendClient(rt) as client:
        enabled = set(
            phases
            or [
                "preflight",
                "project_api",
                "project_claude",
                "project_cursor",
                "reverse",
                "mcp",
                "cursor_kickoff",
                "report",
            ]
        )

        if "preflight" in enabled:
            with step("preflight", rt):
                await driver.preflight(client)

        for spec in PROJECTS:
            phase = f"project_{spec['key']}"
            if phase not in enabled:
                continue
            with step(phase, rt):
                await driver.run_project(client, spec)

        if "reverse" in enabled:
            with step("reverse_upload", rt):
                await driver.reverse_upload(client)
            for prov in ("api", "claude_code", "cursor"):
                with step(f"reverse_{prov}", rt):
                    await driver.reverse_generate(client, prov)

        if "mcp" in enabled:
            with step("mcp_matrix", rt):
                await driver.mcp_exercise(client)

        if "cursor_kickoff" in enabled:
            with step("cursor_ide_kickoff", rt):
                await driver.cursor_ide_kickoff(client)

        if "cursor_verify" in enabled:
            with step("cursor_ide_verify", rt):
                await driver.cursor_ide_verify(client)

        if "report" in enabled:
            with step("report", rt):
                driver.write_report()

    logger.info(banner("E2E DRIVER COMPLETE"))
    fails = [k for k, v in rt.phase_status.items() if not v.startswith("ok")]
    if fails:
        logger.error("Failed phases: %s", ", ".join(fails))
        return 2
    return 0


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--phases", default="", help="Comma-separated subset to run")
    parser.add_argument("--reset", action="store_true", help="Wipe runtime before starting")
    args = parser.parse_args()
    selected = [p.strip() for p in args.phases.split(",") if p.strip()]
    rc = asyncio.run(main(selected or None, args.reset))
    sys.exit(rc)
